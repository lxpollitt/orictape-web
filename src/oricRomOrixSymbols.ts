/**
 * Phase 2 disassembler annotations from the orix-software `.sym` dumps.
 *
 * Source: §6.1 / §6.2 of `oric-asm-rom-v10-v11b-reference.md`, which
 * preserves the upstream `basic10.sym` and `basic11b.sym` files
 * verbatim.  Each entry is a single address → label pairing as published
 * by the orix project.  Used by `disassembler6502.ts`'s `addressLabel`
 * helper *only when the Phase 1 lookup (`SYS_SYMBOLS` exact match or
 * `SYS.PARAMS+N` offset) has no result for the address* — Phase 1
 * substitutions are considered authoritative and short-circuit the
 * Phase 2 fallback.
 *
 * Confidence tiers (surfaced to the user via trailing `?` / `??`
 * markers in the annotation string):
 *
 *   V1.1b (`?`)   — orix's `basic11b.sym` cross-references Whewell's
 *                   Atmos disassembly, so it's treated as moderately
 *                   reliable for V1.1b targets.
 *   V1.0  (`??`)  — orix's `basic10.sym` is less consolidated; §6 of
 *                   the reference doc has documented 4 known mislabels
 *                   in this file (Reset / MusicData / RoundFPA / Delay).
 *                   Those 4 entries are deliberately *omitted* from
 *                   `ORIX_V10` below — we already know they're wrong,
 *                   so we'd rather show nothing than wrong data.  The
 *                   correct addresses for those routines are covered
 *                   by `SYS_SYMBOLS` (Phase 1).
 *
 * The output format is built by {@link lookupOrixAnnotation} and is
 * deliberately compact for a trailing `*` comment slot:
 *
 *   Single ROM only  →  `Name?`     (V1.1b) or `Name??`   (V1.0)
 *   Both ROMs, same  →  `Name?`     (one entry, higher tier marker)
 *   Both ROMs, diff  →  `V11?  / V10??`  (V1.1b first, separated by `/`)
 *
 * The result is appended to the disassembled instruction line as a
 * `*` comment by the disassembler — round-trip-preserving through the
 * assembler (which strips `*` to end-of-line).
 */

