/** Build the trailing-comment annotation string for `addr` from the orix
 *  `.sym` tables.  Returns `null` if neither ROM has an entry.  Caller
 *  (the disassembler) appends this to the line's `*` comment slot only
 *  when Phase 1 substitution produced no result for the same address —
 *  Phase 1 is authoritative and short-circuits this fallback. */
export function lookupOrixAnnotation(addr: number): string | null {
  const v10 = ORIX_V10.get(addr);
  const v11 = ORIX_V11.get(addr);

  if (v10 === undefined && v11 === undefined) return null;

  if (v10 !== undefined && v11 !== undefined) {
    // Both ROMs label this address.
    if (v10 === v11) {
      // Identical label — emit once at the higher tier (V1.1b = `?`).
      // Avoids `Name? Name??` visual noise for invariant entries.
      return `${v11}?`;
    }
    // Different labels — the same address means different routines on
    // the two ROMs, so emit both (it really *is* both, depending on
    // which ROM the code targets).  V1.1b first (higher tier marker),
    // separated by `/` per the user's convention.
    return `${v11}? / ${v10}??`;
  }

  // Only one ROM matches.
  return v11 !== undefined ? `${v11}?` : `${v10}??`;
}
